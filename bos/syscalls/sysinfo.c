#define SYSNAMES_SIZE sizeof(sysnames) / sizeof(sysnames[0])

const char *get_syscall_name(int sys_num) {
  if (sys_num < 0 || sys_num >= SYSNAMES_SIZE || sysnames[sys_num] == NULL) {
    return "unknown";
  }
  return sysnames[sys_num];
}

